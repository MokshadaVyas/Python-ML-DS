# DATATYPES
"""
int = integer (e.g., 1, 100, -5)
float = floating point numbers (e.g., 1.0, -0.5, 3.14)
str = string (e.g., "Hello", 'Python')
bool = booleans (e.g., True, False)
complex = complex numbers (e.g., 1+2j, -3j)
None = NoneType, represents the absence of value
"""

# A VARIABLE is the name given to a memory location in a program

# Examples of different data types
a = 100       # int
b = "Python"  # str
c = 10.20     # float
d = True      # bool
e = 3 + 4j    # complex
f = None      # NoneType

# Print the values
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)

# This will print the type of each variable
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
