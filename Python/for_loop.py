# List of numbers
numbers = [1, 2, 3, 4, 5]

# For loop to iterate over the list
for number in numbers:
    print(number)

# Output:
# 1
# 2
# 3
# 4
# 5

# String
word = "hello"

# For loop to iterate over each character in the string
for char in word:
    print(char)

# Output:
# h
# e
# l
# l
# o

# Tuple of colors
colors = ('red', 'green', 'blue')

# For loop to iterate over the tuple
for color in colors:
    print(color)

# Output:
# red
# green
# blue

# Dictionary
person = {
    "name": "Alice",
    "age": 30,
    "city": "New York"
}

# For loop to iterate over the dictionary keys
for key in person:
    print(key, ":", person[key])

# Output:
# name : Alice
# age : 30
# city : New York

